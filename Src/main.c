// Función para inicializar la media móvil
void inicializar_media_movil(media_movil_t *buffer){
    buffer->suma = 0;
    buffer->indice = 0;
    for(int i = 0; i < BUFFER_SIZE; i++){
        buffer->valores[i] = 100; // Valor inicial (puede ser DISTANCIA_MAX u otro valor predeterminado)
        buffer->suma += buffer->valores[i];
    }
}

// Función para actualizar la media móvil
uint32_t actualizar_media_movil(media_movil_t *buffer, uint32_t nuevo_valor){
    buffer->suma -= buffer->valores[buffer->indice];    //Primero se resta de la suma el anterior valor que se va a sustituir
    buffer->valores[buffer->indice] = nuevo_valor;      //Después sobrescribimos el nuevo valor en el indice actual
    buffer->suma += nuevo_valor;						//Lo añadimos a suma
    buffer->indice = (buffer->indice + 1) % BUFFER_SIZE;//Actualizamos el indice
    return buffer->suma / BUFFER_SIZE;					//Devolvemos la media
}

